/* 
`ip a` และ `grep 192` เป็นคำสั่งในระบบปฏิบัติการ Linux และ macOS ที่ใช้สำหรับแสดงข้อมูลเกี่ยวกับอินเทอร์เฟสเครือข่ายและกรองผลลัพธ์ตามลำดับ

1. `ip a`
- `ip` เป็นคำสั่งในการจัดการการกำหนดค่าและแสดงข้อมูลเกี่ยวกับอินเทอร์เฟสเครือข่าย
- `a` เป็นพารามิเตอร์ย่อของ `address` ซึ่งจะแสดงข้อมูลเกี่ยวกับอินเทอร์เฟสเครือข่ายทั้งหมดบนระบบ รวมถึงที่อยู่ IP, subnet mask, MAC address เป็นต้น

2. `grep 192`
- `grep` เป็นคำสั่งสำหรับค้นหาและกรองข้อความจากอินพุตหรือไฟล์
- `192` เป็นรูปแบบข้อความที่ต้องการค้นหา ในที่นี้คือการค้นหาข้อความที่มี "192" ซึ่งเป็นส่วนหนึ่งของที่อยู่ IP ในรูปแบบ IPv4

ดังนั้น `ip a | grep 192` จะแสดงเฉพาะข้อมูลอินเทอร์เฟสเครือข่ายที่มีที่อยู่ IP ในรูปแบบ IPv4 ที่เริ่มต้นด้วย "192" เท่านั้น ซึ่งมักจะใช้กับระบบเครือข่ายภายในองค์กร (private IP address range)

*/