/* 
คำสั่ง `docker run -d --name ctr1 nginx` เป็นคำสั่งของ Docker CLI ที่ใช้ในการสร้างและเริ่มต้นคอนเทนเนอร์ใหม่ โดยมีความหมายของส่วนประกอบในคำสั่งดังนี้:

- `docker run`: คำสั่งหลักของ Docker ที่ใช้ในการสร้างและเริ่มต้นคอนเทนเนอร์ใหม่
- `-d`: ออปชันที่บอกให้ Docker รันคอนเทนเนอร์ในโหมด detached (background) ซึ่งหมายความว่าคอนเทนเนอร์จะทำงานอยู่เบื้องหลังและไม่แสดงเอาต์พุตบนคอนโซล
- `--name ctr1`: ออปชันที่ใช้ตั้งชื่อให้กับคอนเทนเนอร์ ในที่นี้ตั้งชื่อว่า "ctr1"
- `nginx`: ชื่ออิมเมจที่ใช้เป็นพื้นฐานในการสร้างคอนเทนเนอร์ ในที่นี้ใช้อิมเมจของเว็บเซิร์ฟเวอร์ Nginx

ดังนั้น เมื่อรันคำสั่งนี้ Docker จะสร้างคอนเทนเนอร์ใหม่ตั้งชื่อว่า "ctr1" โดยใช้อิมเมจ Nginx เป็นพื้นฐาน และรันคอนเทนเนอร์ในโหมด detached (ทำงานอยู่เบื้องหลัง)
*/